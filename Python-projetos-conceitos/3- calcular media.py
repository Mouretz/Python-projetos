# 07 - Ler 2 notas, calcular media.
N1 = float(input('Digite a primeira nota >>>'))
N2 = float(input('Digite a segunda nota  >>>'))
#N1 e N2 recebe valor real do usuario.
M = (N1+N2)/2
# A variavel M/Media recebe N1 + N2 e realiza media das notas
print(f'Sua media foi: {M}')
