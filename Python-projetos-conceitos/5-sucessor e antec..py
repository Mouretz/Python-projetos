# 05 - Leia numero inteiro, mostre seu sucessor e antecessor

# NI recebe valor inteiro digitado.
NI = int(input('Digite um numero inteiro: '))
# Exibir na tela o valor inteiro -1 e +1 utilizando saida formatada... \n pula linha.
print(f'O seu Antecessor e: {NI-1} \n E seu Sucessor e:  {NI+1}')